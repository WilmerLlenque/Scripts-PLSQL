---- ARTICULOS SIN FACTOR PROVEEDOR BEBIDAS

SELECT 	A.COD_ITEM,
					A.DESC_ITEM,
				 --P.C_PROVEEDOR,
				 P.X_PROVEEDOR AS PROVEEDOR,
			   A.UM_VENTA,
				 A.UM_COMPRA,
				 A.FACTOR_PROVEEDOR
				 
FROM SYSADM.ARTICULOS A 
			INNER JOIN SYSADM.PROVEEDOR P
	     ON A.PROVEEDOR_DEFAULT=P.C_PROVEEDOR

WHERE PROVEEDOR_DEFAULT = '22770' 
	AND FACTOR_PROVEEDOR IS NULL
	AND STATUS_ITEM = 'A'
	
	
