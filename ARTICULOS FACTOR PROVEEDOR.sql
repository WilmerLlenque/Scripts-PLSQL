---- ARTICULOS A CONSULTAR DE FACTOR POR CARGAR

SELECT 
			NFP.COD_ITEM,
			A.DESC_ITEM,
			A.UM_COMPRA,
			NFP.FACTOR_PROVEEDOR AS FACTOR,
			A.FACTOR_PROVEEDOR

FROM NPAZ.ACTUALIZAR_FACTOR_PROVEEDOR_ARTICULOS NFP
INNER JOIN SYSADM.ARTICULOS A
ON NFP.COD_ITEM = A.COD_ITEM


